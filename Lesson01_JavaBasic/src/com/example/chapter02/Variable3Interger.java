package com.example.chapter02;

public class Variable3Interger {
	//Variable3Interger

	public static void main(String[] args) {
		// byte 타입의 표현범위 -128 ~ 127
		byte byte1 = -128;
		byte byte2 = 127;
		
		System.out.println(byte1);
		System.out.println(byte2);
		
		//long 형의 변수 선언 , 값 뒤에 L,l 을 붙혀준다.
		int num1 = 1234567890;
		long num2 = 12345678900L;
		
		
	}

}
